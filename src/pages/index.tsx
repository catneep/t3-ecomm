import { type NextPage } from "next";
import Head from "next/head";
import Link from "next/link";
import { trpc } from "../utils/trpc";

import Product from "../models/Product";
import ProductCard from "../components/ProductCard";
import Spinner from "../components/Spinner";
import { useRouter } from "next/router";

const Home: NextPage = () => {
  const router = useRouter();
  const allProducts = trpc.products.getAll.useQuery();  
  const postMutation = trpc.products.add.useMutation();
  const deleteMutation = trpc.products.removeAll.useMutation();
  
  const handleNewEntry = async () => {
    // Generates a product with default values
    const testProduct = new Product();

    postMutation.mutateAsync({
      id: testProduct.id,
      name: testProduct.name,
      description: testProduct.description,
      slug: testProduct.slug,
      price: testProduct.price,
      inventory: testProduct.inventory,
    }).then( () => router.reload())
  }

  const handleWipeAll = async () =>
    deleteMutation.mutateAsync()
      .then( () => router.reload());

  return (
    <>
      <Head>
        <title>Create T3 App</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <HeroBanner />

      <main className="container mx-auto my-3 flex flex-col items-center justify-center p-4">

        <div className="flex flex-wrap w-full pt-6 pb-6">
          {
            allProducts.isFetching
            ? <Spinner />
            : allProducts.data && allProducts.data.length > 0
            ? <>
                <h2 className="text-3xl text-gray-700">
                  Our products
                </h2>
                <ul className="w-full px-10 grid sm:grid-cols-2 md:grid-cols-4 gap-4">
                {
                  allProducts.data.map(
                    (product, i) =>
                      <ProductCard product={product} key={i} />
                    )
                  }
                </ul>
              </>
            : <div className="flex flex-wrap w-full justify-center items-center font-bold my-12">
                <h3 className="w-full text-center text-3xl">
                  There&apos;s nothing here! üïµÔ∏è‚Äç‚ôÇÔ∏è
                </h3>
                <h4 className="block w-full text-center text-xl text-gray-500">
                  Try <Link href='/new' className="underline font-semibold">creating a new product</Link>
                </h4>
              </div>
          }
        </div>

      </main>
      <section className=" bg-gray-500 w-full pt-8">
        <header>
          <h2 className="text-center text-3xl text-white">
            Other actions
          </h2>
        </header>
        <div className="flex justify-evenly w-full py-12">
          <button onClick={handleNewEntry}
            className='bg-blue-500 text-white p-6 rounded-lg text-lg'
          >
            ‚úí Register default product
          </button>
          <button onClick={handleWipeAll}
            className='bg-blue-500 text-white p-6 rounded-lg text-lg'
          >
            üî• Wipe all products from DB
          </button>
        </div>
      </section>
    </>
  );
};

export default Home;

